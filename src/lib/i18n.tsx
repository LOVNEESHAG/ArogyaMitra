export type TranslationKey = keyof typeof dictionaries.en;

export function getTranslation(lang: Language, key: string, fallback?: string): string {
  const dictionary = dictionaries[lang] ?? dictionaries[DEFAULT_LANGUAGE];
  if ((dictionary as Record<string, string>)[key]) {
    return (dictionary as Record<string, string>)[key];
  }
  const defaultDictionary = dictionaries[DEFAULT_LANGUAGE] as Record<string, string>;
  if (defaultDictionary[key]) {
    return defaultDictionary[key];
  }
  return fallback ?? key;
}

export type LanguageContextValue = {
  language: Language;
  setLanguage: (lang: Language) => void;
  t: (key: string, fallback?: string) => string;
};

const LanguageContext = React.createContext<LanguageContextValue | null>(null);

function readStoredLanguage(): Language | null {
  try {
    const stored = localStorage.getItem(STORAGE_KEY) as Language | null;
    if (stored && SUPPORTED_LANGUAGES.includes(stored)) return stored;
  } catch {}
  return null;
}

export function LanguageProvider({ children, initialLanguage }: { children: React.ReactNode; initialLanguage?: Language }) {
  // Use a stable default for SSR and initial client render to avoid hydration mismatches
  const [language, setLanguageState] = React.useState<Language>(initialLanguage ?? DEFAULT_LANGUAGE);

  const setLanguage = React.useCallback((lang: Language) => {
    setLanguageState(lang);
    try {
      localStorage.setItem(STORAGE_KEY, lang);
    } catch {}
    if (typeof document !== "undefined") {
      document.documentElement.lang = lang;
      try {
        // Persist a cookie so the server can render the same language on next SSR
        document.cookie = `${STORAGE_KEY}=${lang}; path=/`;
      } catch {}
    }
  }, []);

  // After mount, apply persisted language if available
  React.useEffect(() => {
    const next = readStoredLanguage();
    if (next && next !== language) {
      setLanguage(next);
    } else {
      // Still ensure <html lang> is correct
      if (typeof document !== "undefined") {
        document.documentElement.lang = language;
      }
    }
  // We intentionally run only once on mount
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }, []);

  const t = React.useCallback((key: string, fallback?: string) => getTranslation(language, key, fallback), [language]);

  const value = React.useMemo(() => ({ language, setLanguage, t }), [language, setLanguage, t]);

  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>;
}

export function useLanguage(): LanguageContextValue {
  const ctx = React.useContext(LanguageContext);
  if (!ctx) throw new Error("useLanguage must be used within a LanguageProvider");
  return ctx;
}

export function useTranslation() {
  const { t, language, setLanguage } = useLanguage();
  return { t, language, setLanguage };
}
